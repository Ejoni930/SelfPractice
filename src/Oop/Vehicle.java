package Oop;

public class Vehicle {
    int body;
    String model;
    String type;
    String color;
    int year;
    String vinNumber;
    public Vehicle(int body,String model,String type,String color,String vinNumber,int year){
        this.body=body;
        this.model=model;
        this.type=type;
        this.color=color;
        this.vinNumber=vinNumber;
        this.year=year;
    }

}
