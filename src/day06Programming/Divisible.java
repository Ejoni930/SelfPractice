package day06Programming;

public class Divisible {
    public static void main(String[] args) {
        int number =65;
        if(number%2==0){
            System.out.println(+number+" divisible by two : true");
        }else{
            System.out.println(+number+" divisible by two : false");
        }
        if(number%3==0){
            System.out.println(+number+" divisible by two : true");
        }else{
            System.out.println(+number+" divisible by two : false");
        }
        if(number%5==0){
            System.out.println(+number+" divisible by two : true");
        }else{
            System.out.println(+number+" divisible by two : false");
        }
    }
}

