package day01Programming;

import com.sun.xml.internal.ws.addressing.WsaActionUtil;

public class Numers1_100 {
    public static void main(String[] args) {
        int i;
        //even numbers
        for(i=0;i<=100;i=i+2){
            System.out.println(i);
        }
        //0-100
        for(i=0;i<=100;i++){
            System.out.println(i);
        }

        //odd numbers
        for(i=1;i<=100;i=i+2){
            System.out.println(i);
        }
    }
}
